export const STUDY_ALL: StudyItem[] = [
  ...STUDY_CORE,
  ...STUDY_GROUPS.flatMap((g) => g.items),
];

export function getStudyArticle(slug: string): StudyItem | null {
  return STUDY_ALL.find((a) => a.slug === slug) ?? null;
}

export function findStudyContext(slug: string): {
  item: StudyItem;
  groupTitle: string;
  prev: StudyItem | null;
  next: StudyItem | null;
} | null {
  const idx = STUDY_ALL.findIndex((a) => a.slug === slug);
  if (idx < 0) return null;
  const item = STUDY_ALL[idx];
  const prev = idx > 0 ? STUDY_ALL[idx - 1] : null;
  const next = idx < STUDY_ALL.length - 1 ? STUDY_ALL[idx + 1] : null;
  const inGroup = STUDY_GROUPS.find((g) => g.items.some((i) => i.slug === slug));
  const groupTitle = inGroup ? inGroup.title : "필수";
  return { item, groupTitle, prev, next };
}
